/* Exercício 4: Calculando Média
Crie 3 variáveis numéricas:

nota1, nota2 e nota3, representando 3 notas de um aluno.
Crie uma variável media que calcule a média das 3 notas e imprima o resultado no console. */

let nota1 = 8.9;
let nota2 = 7.5;
let nota3 = 3.5;

media = (nota1 + nota2 + nota3) / 3;
media = media.toFixed(2);

console.log(`a sua media é : ${media}`)